"""피드백 생성 프롬프트"""

def get_feedback_system_prompt(provider: str, is_single_topic:bool = False) -> str:
    """Provider에 맞는 시스템 프롬프트 반환"""
    if is_single_topic:
        return SINGLE_TOPIC_SYSTEM_PROMPT.get(provider)
    return MULTI_TOPIC_SYSTEM_PROMPT.get(provider)

