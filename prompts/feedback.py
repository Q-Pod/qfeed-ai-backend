def _format_category(category: QuestionCategory | None) -> str:
    """카테고리를 문자열로 포맷팅"""
    if category is None:
        return "N/A"
    return category.value if hasattr(category, 'value') else str(category)


def build_real_mode_feedback_prompt(
    question_type: str,
    category: str,
    grouped_interview: dict[int, dict],
) -> str:
    """피드백 생성용 프롬프트 생성 (XML 포맷 적용)"""
    interview_sections = []
    
    for topic_id, topic_data in sorted(grouped_interview.items()):
        category_val = topic_data.get("category")
        category_str = _format_category(category_val)
        main_question = topic_data.get("main_question", "")
        
        # XML 태그 시작
        section_lines = [
            f'<topic id="{topic_id}" category="{category_str}">',
            f'  <main_question>{main_question}</main_question>'
        ]
        
        # topic_data 내에 'turns' 리스트가 존재한다고 가정
        # (만약 기존에 qa_text만 있었다면, 백엔드 그룹핑 로직에서 turns 리스트를 넘겨주도록 수정 필요)
        turns = topic_data.get("turns", [])
        
        if turns:
            for turn in turns:
                turn_order = turn.get("turn_order", 0)
                turn_type = turn.get("turn_type", "unknown")
                q = turn.get("question", "")
                a = turn.get("answer_text", "")
                
                section_lines.extend([
                    f'  <turn order="{turn_order}" type="{turn_type}">',
                    f'    <Q>{q}</Q>',
                    f'    <A>{a}</A>',
                    '  </turn>'
                ])
        else:
            # 하위 호환성: turns가 없고 기존처럼 qa_text만 있는 경우
            qa_text = topic_data.get("qa_text", "")
            section_lines.append(f'  <qa_text>\n{qa_text}\n  </qa_text>')
            
        section_lines.append('</topic>')
        interview_sections.append("\n".join(section_lines))
    
    interview_text = "\n\n".join(interview_sections)

    return f"""다음 면접 세션을 분석하여 토픽별 피드백과 종합 피드백을 작성해주세요.

[질문 유형] {question_type}

[면접 히스토리]
{interview_text}

위 내용을 바탕으로:
1. 각 <topic> 내의 개별 <turn> 단위에 맞춘 상세 피드백
2. 전체 면접에 대한 종합 피드백
을 지정된 JSON 구조에 맞게 작성해주세요."""

