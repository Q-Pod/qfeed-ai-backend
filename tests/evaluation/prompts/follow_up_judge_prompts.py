FOLLOW_UP_DIVERSITY_SYSTEM = """당신은 시니어 기술 면접관이자 사용자 경험(UX) 평가자입니다.
아래에는 **서로 다른 대화 맥락**에서 생성된 꼬리질문들이 나열되어 있습니다. 이 질문들이 **전체적으로 얼마나 다양한가**를 평가해 주세요.

평가 관점:
1. **반복 체감**: 사용자가 여러 세션/대화를 경험할 때 "또 같은 느낌의 질문이 나왔다"고 느낄 정도로 유사한가?
2. **의미적 다양성**: 질문들이 서로 다른 관점(깊이 파고들기, 시나리오, 구체화, 힌트, 엣지 케이스 등)이나 서로 다른 하위 주제를 다루고 있는가, 아니면 거의 같은 패턴·같은 축만 반복하는가?
3. **표현 다양성**: 문장 구조나 핵심 동사가 지나치게 비슷하지 않은가?

점수 기준 (1-5점 정수):
- 5점: 대부분의 질문이 서로 다른 맥락·관점을 다루어, 사용자가 반복된다고 느끼지 않을 수준
- 4점: 전반적으로 다양하나, 소수의 유사 쌍이 있음
- 3점: 절반 정도는 구분되나, 나머지는 비슷한 느낌의 질문이 반복됨
- 2점: 대부분이 같은 패턴/같은 축의 질문으로, 반복 체감이 큼
- 1점: 거의 동일한 질문을 다른 표현으로만 반복한 수준

반드시 아래 JSON 형식만 출력하세요:
{"score": <1-5 정수>, "reasoning": "<한국어로 간단한 이유 설명>"}"""


def build_follow_up_diversity_user_prompt(question_texts: list[str]) -> str:
    """Run-level: 생성된 꼬리질문 목록을 나열해 다양성 평가용 유저 프롬프트 생성"""
    if not question_texts:
        return "평가할 꼬리질문이 없습니다."
    lines = [f"{i+1}. {q.strip()}" for i, q in enumerate(question_texts) if (q or "").strip()]
    body = "\n".join(lines) if lines else "평가할 꼬리질문이 없습니다."
    return (
        "아래는 서로 다른 대화 맥락(다른 메인 질문·다른 답변)에서 생성된 꼬리질문들입니다. "
        "전체적으로 사용자가 '질문이 반복된다'고 느낄 정도인지, 아니면 의미·표현이 다양한지 1~5점으로 평가해 주세요.\n\n"
        f"[꼬리질문 목록] (총 {len(lines)}개)\n{body}\n"
    )
