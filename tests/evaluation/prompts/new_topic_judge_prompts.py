def _format_history(interview_history: list[dict]) -> str:
    """간단한 히스토리 요약 문자열 생성"""
    if not interview_history:
        return "이전 대화 없음 (첫 질문)."
    lines = []
    for i, turn in enumerate(interview_history):
        q = (turn.get("question") or "").strip()
        t = turn.get("turn_type") or ""
        if not q:
            continue
        lines.append(f"{i+1}. [{t}] {q}")
    if not lines:
        return "이전 대화 없음 (첫 질문)."
    return "이전 대화 요약:\n" + "\n".join(lines)


def build_tradeoff_user_prompt(question_type: str, question_text: str, interview_history: list[dict]) -> str:
    history_str = _format_history(interview_history)
    return (
        "다음은 기술 면접에서 사용될 new_topic 메인 질문입니다.\n"
        "이 질문이 단순 지식 정의가 아니라, Trade-off(선택과 타협)를 얼마나 잘 묻고 있는지 평가해 주세요.\n\n"
        f"[질문 유형]\n{question_type}\n\n"
        f"[이전 히스토리]\n{history_str}\n\n"
        f"[메인 질문]\n{question_text}\n"
    )


